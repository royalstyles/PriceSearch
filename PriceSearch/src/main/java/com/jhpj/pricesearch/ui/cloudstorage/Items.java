package com.jhpj.pricesearch.ui.cloudstorage;

public class Items {
    String name;
    String imagUrl;

    public Items(String name, String imagUrl) {
        this.name = name;
        this.imagUrl = imagUrl;
    }

    public String getName() {
        return name;
    }

    public String getImagUrl() {
        return imagUrl;
    }
}
